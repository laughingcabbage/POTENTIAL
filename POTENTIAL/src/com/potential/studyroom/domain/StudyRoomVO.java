package com.potential.studyroom.domain;

import com.hr.cmn.DTO;

public class StudyRoomVO extends DTO {
	private String ID;
	private int seatNum;
	private String startTime;
	private String endTime;
	private int extension;
	
	
	public StudyRoomVO(String iD, int seatNum, String startTime, String endTime, int extension) {
		super();
		ID = iD;
		this.seatNum = seatNum;
		this.startTime = startTime;
		this.endTime = endTime;	
		this.extension = extension;
	}


	/**
	 * @return the iD
	 */
	public String getID() {
		return ID;
	}


	/**
	 * @param iD the iD to set
	 */
	public void setID(String iD) {
		ID = iD;
	}


	/**
	 * @return the seatNum
	 */
	public int getSeatNum() {
		return seatNum;
	}


	/**
	 * @param seatNum the seatNum to set
	 */
	public void setSeatNum(int seatNum) {
		this.seatNum = seatNum;
	}


	/**
	 * @return the startTime
	 */
	public String getStartTime() {
		return startTime;
	}


	/**
	 * @param startTime the startTime to set
	 */
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	/**
	 * @return the endTime
	 */
	public String getEndTime() {
		return endTime;
	}


	/**
	 * @param endTime the endTime to set
	 */
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	
	
	/**
	 * @return the startTime
	 */
	public int getExtension() {
		return extension;
	}


	/**
	 * @param startTime the startTime to set
	 */
	public void setExtension(int extension) {
		this.extension = extension;
	}

	
	@Override
	public String toString() {
		return (ID != null ? "ID=" + ID + ", " : "")
				+ "seatNum=" + seatNum + ", "
				+ (startTime != null ? "startTime=" + startTime + ", " : "")
				+ (endTime != null ? "endTime=" + endTime + ", " : "")
				+ "extension=" + extension;
	}
	
	
}
